    public void lotNameByBlock(String block){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Lot> list = LotDao.getAllRecordsByBlock(block);
        Iterator<Lot> itx = list.iterator();
        while (itx.hasNext()) {
            Lot lotObj = itx.next();
            dtm.addRow(new Object[]{lotObj.getName()});
        }
    }
    
    public void filterLotByName (String name, String block){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Lot> list = LotDao.filterLotByname(name,block);
        Iterator<Lot> itx = list.iterator();
        while (itx.hasNext()) {
            Lot lotObj = itx.next();
            dtm.addRow(new Object[]{lotObj.getName()});
        }
    }
    
