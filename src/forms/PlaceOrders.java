    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String price = txtPrice.getText();
        String quantity = String.valueOf(jSpinner1.getValue());


        // Validate fields before adding to the table
        if (!name.isEmpty() && !price.isEmpty() && !quantity.isEmpty()) {
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.addRow(new Object[]{name, price, quantity, lotTotal});

            grandTotal += lotTotal;
            lblGrandTotal.setText(String.valueOf(grandTotal));
            
            btnGenerateBillPrint.setEnabled(true);


            clearLotFields();
            validateField();
        } else {
            JOptionPane.showMessageDialog(this, "Please fill all fields before adding to cart.");
        }

    }//GEN-LAST:event_btnAddToCartActionPerformed

